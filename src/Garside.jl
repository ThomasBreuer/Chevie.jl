function centralizer_generators(b,F=(x,y=1)->x;ss::Symbol=:sc)
  if ss==:ss || ss==:sc
    b=representativeSC(b,F)
    a=b.conj
    b=b.circuit[1]
    Ref(a).*endomorphisms(conjcat(b,F;ss=ss),1).*Ref(a^-1)
  else
    endomorphisms(conjcat(b,F;ss=ss),1)
  end
end

