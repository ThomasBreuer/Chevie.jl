"""
elts must be simples
"""
function leftgcd(M::LocallyGarsideMonoid{T},elts::T...)where T
  x=one(M)
  found=true
  while found
    found=false
    for i in eachindex(M.atoms)
      if all(b->isleftdescent(M,b,i),elts)
        found=true
        x=mul!(M,x,M.atoms[i])
        elts=map(x->\(M,M.atoms[i],x),elts)
      end
    end
  end
  return x,elts
end

function rightgcd(M::LocallyGarsideMonoid{T},elts::T...)where T
  x=one(M)
  found=true
  while found
    found=false
    for i in eachindex(M.atoms)
      if all(b->isrightdescent(M,b,i),elts)
        found=true
        x=*(M,M.atoms[i],x)
        elts=map(x->/(M,x,M.atoms[i]),elts)
      end
    end
  end
  return x,elts
end

function rightlcm(M::GarsideMonoid{T},elts::T...)where T
  x,c=rightgcd(M,rightcomplŒ¥.(Ref(M),elts)...)
  *(M,elts[1],c[1]),c
end

"""
returns Œ±(xv),œâ(xv)
"""
function alpha2(M::GarsideMonoid,x,v)
  g,rests=leftgcd(M,\(M,x,M.delta),v)
  (*(M,x,g),rests[2])
end

function (M::GarsideMonoid{T})(l::Integer...)where T
  res=GarsideElm(0,T[],M)
  if isempty(l) return res end
  for s in reverse(l) # faster in reversed order (see *)
    if s<0 b=inv(GarsideElm(0,[M.atoms[-s]],M))
    else   b=GarsideElm(0,[M.atoms[s]],M)
    end
    res=b*res
  end
  res
end

function (M::GarsideMonoid{T})(r::T)where T
  if r==one(M) GarsideElm(0,T[],M)
  elseif r==M.delta GarsideElm(1,T[],M)
  else GarsideElm(0,[r],M)
  end
end

