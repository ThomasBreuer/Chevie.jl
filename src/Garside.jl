function CoxGroups.word(M::GarsideMonoid{T},w::T)where T
  res=Int[]
  while w!=one(M)
    for i in eachindex(M.atoms)
       if isleftdescent(M,w,i)
         push!(res,i)
         w=\(M,M.atoms[i],w)
       end
    end
  end
  res
end

