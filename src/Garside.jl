function fraction(b::GarsideElm)
  M=b.M
  if b.pd>=0 return [one(b),b] end
  if -b.pd>length(b.elm) return [inv(b),one(b)] end
  GarsideElm(0,inv(b).elm[length(b.elm)+b.pd+1:length(b.elm)],M),
  GarsideElm(0,b.elm[1-b.pd:length(b.elm)],M)
end;

"""
